export function t2s(text: string): string {
  let result = "";
  for (const ch of text) {
    result += charMap.get(ch) || ch;
  }
  return result;
}

export function t2sDeep<T>(obj: T): T {
  if (typeof obj === "string") return t2s(obj) as T;
  if (Array.isArray(obj)) return obj.map(t2sDeep) as T;
  if (obj && typeof obj === "object") {
    const result: any = {};
    for (const [key, val] of Object.entries(obj)) {
      result[key] = t2sDeep(val);
    }
    return result as T;
  }
  return obj;
}
